export const getFilePreview = async (fileId, type) => {
  let fileUrl;

  try {
    if (type === "video") {
      fileUrl = storage.getFileView(config.storageId, fileId);
    } else if (type === "image") {
      fileUrl = storage.getFilePreview(
        config.storageId,
        fileId,
        // width
        2000,
        // height
        2000,
        // gravity
        "top",
        //Quality
        100
      );
    } else {
      throw new Error("Invalid File type");
    }

    if (!fileUrl) throw Error;

    // console.log("File URL:", fileUrl); // Log fileUrl

    return fileUrl;
  } catch (error) {
    console.error("getFilePreview Error:", error.message); // Log error
    throw new Error(error);
  }
};

export const uploadFile = async (file, type) => {
  if (!file) return;

  const { mimeType, ...rest } = file;
  const asset = {
    type: file.mimeType,
    ...rest,
  };

  // console.log("FILE", file);
  try {
    const uploadedFile = await storage.createFile(
      config.storageId,
      ID.unique(),
      asset
    );
    // console.log("UPLOADED", uploadedFile);

    const fileUrl = await getFilePreview(uploadedFile.$id, type);

    return fileUrl;
  } catch (error) {
    console.error("uploadFile Error:", error.message); // Log error
    throw new Error(error);
  }
};

export const createVideo = async (form) => {
  try {
    const [thumbnailUrl, videoUrl] = await Promise.all([
      uploadFile(form.thumbnail, "image"),
      uploadFile(form.video, "video"),
    ]);

    const newPost = await databases.createDocument(
      config.databaseId,
      config.videoCollectionId,
      ID.unique(),
      {
        title: form.title,
        thumbnail: thumbnailUrl,
        video: videoUrl,
        prompt: form.prompt,
        creator: form.userId,
      }
    );

    return newPost;
  } catch (error) {
    console.error("createVideo Error:", error.message); // Log error
    throw new Error(error);
  }
};

